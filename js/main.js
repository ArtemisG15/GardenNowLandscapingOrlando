/* ============================================================
   CONTACT FORM — Netlify Forms (AJAX submission)
   Netlify detects data-netlify="true" at build time and processes
   submissions server-side. Configure notification email in:
   Netlify dashboard > Site > Forms > Notifications
============================================================ */
const form        = document.getElementById('contactForm');
const formSuccess = document.getElementById('formSuccess');

form.addEventListener('submit', async e => {
  e.preventDefault();

  const submitBtn = form.querySelector('[type="submit"]');
  submitBtn.disabled = true;

  try {
    const response = await fetch('/', {
      method: 'POST',
      headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
      body: new URLSearchParams(new FormData(form)).toString()
    });

    if (response.ok) {
      formSuccess.classList.add('visible');
    } else {
      throw new Error('Network response was not ok');
    }
  } catch {
    /* Fallback: open WhatsApp with pre-filled message */
    const name    = form.querySelector('[name="name"]').value;
    const phone   = form.querySelector('[name="phone"]').value;
    const service = form.querySelector('[name="service"]').value;
    const msg     = form.querySelector('[name="message"]').value;
    const waText  = encodeURIComponent(
      `Olá! Me chamo ${name}.\nTelefone: ${phone}\nServiço: ${service}\nMensagem: ${msg}`
    );
    formSuccess.classList.add('visible');
    setTimeout(() => window.open(`https://wa.me/14079141712?text=${waText}`, '_blank'), 800);
  } finally {
    submitBtn.disabled = false;
  }
});


/* ============================================================
   BEFORE / AFTER SLIDERS
============================================================ */
function initBeforeAfterSliders() {
  document.querySelectorAll('.ba-slider').forEach(slider => {
    const before = slider.querySelector('.ba-before');
    const handle = slider.querySelector('.ba-handle');
    let dragging = false;

    const setPos = clientX => {
      const r = slider.getBoundingClientRect();
      const pct = Math.max(5, Math.min(95, (clientX - r.left) / r.width * 100));
      before.style.clipPath = `inset(0 ${100 - pct}% 0 0)`;
      handle.style.left     = pct + '%';
    };

    /* Mouse */
    slider.addEventListener('mousedown', e => { dragging = true; setPos(e.clientX); });
    window.addEventListener('mouseup',   () => { dragging = false; });
    window.addEventListener('mousemove', e => { if (dragging) setPos(e.clientX); });

    /* Touch */
    slider.addEventListener('touchstart', e => { dragging = true; setPos(e.touches[0].clientX); }, { passive: true });
    window.addEventListener('touchend',   () => { dragging = false; });
    window.addEventListener('touchmove',  e => { if (dragging) setPos(e.touches[0].clientX); }, { passive: true });
  });
}


